function closeForm() {
  var addTaskForm = document.getElementById("addSystem");
  if (addTaskForm.style.display === "none") {
    addTaskForm.style.display = "block";
  } else {
    addTaskForm.style.display = "none";
  }
}


function addFormAppear() {
	var addTaskForm = document.getElementById("addSystem");
	if(addTaskForm.style.display === "none") {
		addTaskForm.style.display = "block"
	}
}