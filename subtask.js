function addSubtask() {
  var addSubtaskForm = document.getElementById("addSubtask");
  if (addSubtaskForm.style.display === "none") {
    addSubtaskForm.style.display = "block";
  } else {
    addSubtaskForm.style.display = "none";
  }
}


function addFormAppear() {
	var addSubtaskForm = document.getElementById("addSubtask");
	if(addSubtaskForm.style.display === "none") {
		addSubtaskForm.style.display = "block"
	}
}